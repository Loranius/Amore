-- ============================================================
-- МІГРАЦІЯ: wishlist_items.is_shared — спільні бажання
-- ------------------------------------------------------------
-- Нова колонка позначає бажання як видиме ОБОМ незалежно від
-- власника (третя категорія на додачу до «моє»/«партнера»).
-- owner лишається — це автор запису; is_shared лише розширює
-- видимість, не змінює семантику власності/прав редагування.
--
-- Запустити ОДИН РАЗ у Supabase SQL Editor ПЕРЕД деплоєм React-версії
-- з новою вкладкою «Спільне». Ідемпотентна: add column if not exists.
-- ============================================================
alter table public.wishlist_items
  add column if not exists is_shared boolean not null default false;

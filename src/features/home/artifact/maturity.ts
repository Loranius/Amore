/**
 * Асимптотична крива 0→1: свіжовиниклий вузол (age=0) — тонкий тупий
 * паросток; що більше минуло часу — то товщий/гостріший/стабільніший
 * (age → ∞ ⇒ maturity → 1, ніколи не «завершується» остаточно — завжди
 * трошки ще росте, як і справжній мінерал). Вік завжди рахується від
 * РЕАЛЬНОЇ дати в БД — це і є «Evolution Memory»: жодного окремого стану,
 * лише переобчислення з фактів.
 */
export function maturityCurve(ageDays: number, halfLife = 18): number {
  return 1 - Math.exp(-Math.max(0, ageDays) / halfLife);
}
